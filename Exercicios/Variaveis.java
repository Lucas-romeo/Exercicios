public class Variaveis {
    public static void main(String[] args) {
        int idade = 21;
        double altura = 1.80;
        boolean clt = true;
        String nome = "Lucas";

        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Trabalha?: " + clt);
        
    }
}
